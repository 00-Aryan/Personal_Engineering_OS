"""Tests for real_api_smoke.py."""

from __future__ import annotations

import json
import os
from unittest.mock import patch, MagicMock
from pathlib import Path

from scripts.real_api_smoke import main

def test_smoke_skips_cleanly_without_api_key(tmp_path: Path) -> None:
    """Verify script skips cleanly and returns 0 when no API key is available."""
    results_file = tmp_path / "real_api_smoke_results.json"
    
    with patch("scripts.real_api_smoke.RESULTS_PATH", results_file), \
         patch.dict(os.environ, {"GEMINI_API_KEY": ""}, clear=True):
         
        exit_code = main()
        assert exit_code == 0
        assert results_file.exists()
        
        data = json.loads(results_file.read_text(encoding="utf-8"))
        assert data["status"] == "SKIPPED"

