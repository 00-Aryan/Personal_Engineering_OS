def test_budget_enforcement_without_api_call() -> None:
    """Verify that budget limit enforcement prevents any real API call."""
    from core.model_provider import GeminiProvider
    from core.observability.token_budget import TokenBudget
    
    provider = GeminiProvider(agent_name="clone")
    
    custom_budgets = {
        "test_agent": {
            "hard_limit_per_call": 10
        }
    }
    tb_test = TokenBudget(state_dir=Path(".projectos_state"), budgets=custom_budgets)
    
    with patch.object(provider, "_complete_once") as mock_complete_once:
        prompt_50_tokens = "A" * 200
        response = provider.complete(
            prompt=prompt_50_tokens,
            system_prompt="",
            max_tokens=5,
            agent_name="test_agent",
            token_budget=tb_test
        )
        
        assert "TOKEN_BUDGET_EXCEEDED" in response
        mock_complete_once.assert_not_called()
